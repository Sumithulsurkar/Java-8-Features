package com.interviews;

public class IntegerComparison {
    public static void main(String[] args) {

        Integer i1 = 50;
        Integer i2 = 50;
        Integer i3 = 200;
        Integer i4 = 200;

        if(i1 == i2) {
            System.out.println("i1 == i2");
        } else
            System.out.println("i1 != i2");
        if(i3 == i4) {
            System.out.println("i3 == i4");
        } else
            System.out.println("i3 != i4");
    }
}
